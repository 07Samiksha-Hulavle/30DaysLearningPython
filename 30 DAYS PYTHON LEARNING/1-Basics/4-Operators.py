# 1)Arithmetic Operators:

num1 = 8
num2 = 4

print(num1 + num2)  #Addition
print(num1 - num2)  #Subtraction
print(num1 * num2)  #Multipication
print(num1 / num2)  #Division 
print(num1 // num2) #Floor Division 
print(num1 % num2)  #Modulus: (gives remainder)
print(num1 ** num2) #Exponent (Power)

# 2)Assignment Operators:

num = 50
print(num)

num += 10 #add and assign
print(num)

# 3)Comparison Operators:

p1 = 30
p2 = 10

print(p1 == p2) #Equal to
print(p1 != p2) #Not Equalt to
print(p1 >= p2) 
print(p1 <= p2) 
print(p1 > p2)
print(p1 < p2)


